    public function identifyUser(){
        $user= auth('sanctum')->user();
        if ($user) {
            return response()->json([
                'status' => true,
                'message' => 'User Authenticated Successfully',
                'user' => $user
            ], 200);
        } else {
            return response()->json([
                'status' => false,
                'message' => 'User Not Authenticated'
            ], 401);
        }
    }

